def add_text(slide, text, left, top, width, height, size=20, bold=False,
             color=DARK, align=PP_ALIGN.RIGHT):
    box = slide.shapes.add_textbox(Inches(left), Inches(top),
                                   Inches(width), Inches(height))
    tf = box.text_frame
    tf.word_wrap = True
    first = True
    for line in text.split("\n"):
        p = tf.paragraphs[0] if first else tf.add_paragraph()
        first = False
        p.text = line
        p.alignment = align
        for run in p.runs:
            run.font.size = Pt(size)
            run.font.bold = bold
            run.font.color.rgb = color
    return box


def slide_title(title, subtitle="", size=40):
    s = prs.slides.add_slide(BLANK)
    add_text(s, title, 0.6, 0.35, 12.1, 1.0, size=size, bold=True, color=DARK)
    if subtitle:
        add_text(s, subtitle, 0.6, 1.25, 12.1, 0.6, size=18, color=ACCENT)
    return s


def slide_bullets(title, bullets, subtitle="", size=20):
    s = slide_title(title, subtitle)
    add_text(s, "\n".join("• " + b for b in bullets), 0.9, 2.0, 11.5, 5.0,
             size=size)
    return s


def slide_image(title, img, subtitle="", img_top=1.9, img_h=5.2):
    s = slide_title(title, subtitle)
    img = P / img
    if img.exists():
        from PIL import Image
        w, h = Image.open(img).size
        disp_h = img_h
        disp_w = disp_h * w / h
        if disp_w > 12.3:
            disp_w = 12.3
            disp_h = disp_w * h / w
        s.shapes.add_picture(str(img), Inches((13.333 - disp_w) / 2),
                             Inches(img_top), Inches(disp_w), Inches(disp_h))
    else:
        add_text(s, f"[חסר: {img.name}]", 4, 3.5, 5, 1, size=14)
    return s


